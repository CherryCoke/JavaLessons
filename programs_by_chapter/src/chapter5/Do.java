package chapter5;

public class Do {
	public static void main(String args[]){
		
		//Creating a variable
		int x = 0;
		
		//Do the following 
		do {
			System.out.println(x); //Print x
			x++; //increase x by one
		} while (x < 10); //While x is less than 10
	}
}
