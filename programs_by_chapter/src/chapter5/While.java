package chapter5;

public class While {
	public static void main(String args[]){
		
		//Creating arbitrary variable
		int x = 0;
		
		//While x is less than or equal to ten
		while(x <= 10){
			System.out.println(x); //Print x
			x++; //Increase x by one
		}
	}
}
